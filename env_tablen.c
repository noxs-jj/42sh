#include "sh.h"

int		env_tablen(char **tab)
{
	int	i;
	int	ret;

	i = 0;
	ret = 0;
	while (tab[i] != NULL)
	{
		ret = ret + ft_strlen(tab[i]);
		i++;
	}
	return (ret);
}
